def maNganh(name):
    if name == 'Y khoa':
        return '7720101'
    elif name == 'Dược học (tổ hợp A00)':
        return '7720201A'
    elif name == 'Dược học (tổ hợp B00)':
        return '7720201B'
    elif name == 'Điều dưỡng':
        return '7720301'
    elif name == 'Răng - Hàm - Mặt':
        return '7720501'
    else:
        return ''
